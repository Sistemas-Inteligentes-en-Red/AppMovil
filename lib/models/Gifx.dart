class Gifx {
  int id;
  String direccion;
  String cliente;
  String nombre;
  String telefono;
  String tinicio;
  String tfinal;
  String hora;
  String estado;

  Gifx(
      id, direccion, cliente, nombre, telefono, tinicio, tfinal, hora, estado) {
    this.id = id;
    this.direccion = direccion;
    this.cliente = cliente;
    this.nombre = nombre;
    this.telefono = telefono;
    this.tinicio = tinicio;
    this.tfinal = tfinal;
    this.hora = hora;
    this.estado = estado;
  }
}
